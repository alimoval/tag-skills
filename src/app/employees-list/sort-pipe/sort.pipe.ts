import { Pipe, PipeTransform } from '@angular/core';

@Pipe({
  name: 'sort'
})
export class SortPipe implements PipeTransform {

  transform(array: Employee[], args?: string): Employee[] {
    if (!args) {
      return array;
    }
    if (args === 'backward') {
      array.sort((a: Employee, b: Employee) => {
        if (a.name < b.name) {
          return 1;
        } else if (a.name > b.name) {
          return -1;
        } else {
          return 0;
        }
      });
      return array;
    }
    array.sort((a: Employee, b: Employee) => {
      if (a.name < b.name) {
        return -1;
      } else if (a.name > b.name) {
        return 1;
      } else {
        return 0;
      }
    });
    return array;
  }
}
